// using pop() method
var colors = ["Red", "Green", "Blue", "Purple", "yellow"]
document.write(colors+"<br/>");
var last = colors.pop();
document.write(last+"<br/>");
document.write(colors+"<br/>");

// using shift() method
var dailyActivities = ['work', 'eat', 'sleep', 'exercise'];
dailyActivities.shift();
document.write(dailyActivities);
